        <p>
            <b>Da educação depende a conduta de toda a vida. (MC)</b>
        </p>

        <p>
            O Jardim Canossa nasceu de uma história iniciada em 1978 com a 
            chegada das Irmãs Canossianas na Vila Nova, Imperatriz. No início 
            dos anos 90 as religiosas mudaram a residência para o Bairro Parque 
            Santa Lúcia, periferia de Imperatriz e iniciou um trabalho voltado 
            para a promoção humana e social das pessoas em situação de 
            vulnerabilidade e risco social e especialmente para a recuperação 
            de crianças com alto nível de desnutrição.
        </p>

        <p>
            No decorrer da realização do projeto para as crianças com 
            deficiência alimentar, havia um número expressivo de crianças na 
            faixa etária da Educação Infantil, mas só lhes restavam à rua como 
            espaço social, e com sérios riscos de violência. Em 1999 no mês de 
            agosto as Irmãs Canossianas, iniciaram o processo de legalização de 
            uma escola de Educação Infantil.
        </p>

        <p>
            EDUCAR é a primeira ação da caridade Canossiana, pois “em educação 
            a coisa mais importante é a formação do coração”. A nossa ação 
            educativa é FORMAR a partir de dentro, para os valores humanos, 
            cristãos, éticos e morais. Porque educar quer dizer: fazer conhecer 
            a uma pessoa as suas possibilidades.</p>

        <p>É necessário que o homem e a mulher reconheçam seu valor como 
            “pessoa humana”, dotada de capacidades, de possibilidades, de dons, 
            só assim compreenderá que também é amado, se descobre ‘pessoa’ 
            amável, com riqueza e capaz de amar. A estrutura física era bem 
            menor, dispunha-se de uma sala, cozinha e uma varanda, com o início 
            da escola contavam com apenas três salas de aula, hoje a escola 
            conta com cinco salas de aula além de outros espaços: sala 
            interativa, brinquedoteca, salão, etc. Beneficiando um total de 228 
            crianças na faixa etária de 03 a 05 anos no Curso de Educação 
            Infantil.
        </p>
    </div>
    <div data-idioma="eng" id="conteudo-coluna-2">
        <p>
            <b>From education depends the conduct of the entire life. (MC)</b>
        </p>
        <p>
            The Jardim Canossa was born from a story that begun in 1978 with the
            arrival of the Canossian Sisters in Vila Nova, Imperatriz. In the 
            early 90's they moved the residence to Parque Santa Lúcia district, 
            outskirts of Imperatriz and bugun a work aimed at human and social
            promotion of people in vulnerable situation, social risk, and
            especially at the recovery of children under a high level of
            malnutrition.
        </p>
        <p>
            Along the execution of the project to the children with food
            deficiency, there was an expressive number of children in the age
            group of child education, but they had only the streets as social
            space, and under serious risks of violence. In august, 1999, the
            Canossian Sisters, begun the legalization process of a child
            education school.
        </p>
        <p>
            TO EDUCATE is the first canossian act of charity, for "in education,
            the most important thing is the heart's formation". Our educative
            activity is TO SHAPE from inside, for human, christian, moral and
            ethic values. Because educate means: make known a person and it's
            possibilities.
        </p>

        <p>
            It is necessary that a man and a woman get to acknowledge their
            value as "human being", gifted with capabilities and possibilities,
            only then will they understand they are loved. Finds themselves as
            kind, rich, and able to love persons. The physical structure were
            way smaller, disposed of one room, kitchen and veranda, counted on
            the beginning of the school with only three class rooms. Today there
            are five class rooms, apart from other spaces like the interactive
            room, toy library ahd hall. Benefiting a sum of 228 children from
            3 to 5 years old in the child education course.
        </p>
