        <p>
            <b>Canossian Daughters of Charity Congregation - Canossian Sisters</b>
        </p>

        <p>
            The Canossian Sisters were founded by Magdalene of Canossa, in
            Verona, Italy, on march 8<sup>th</sup> 1808. They develop activities
            in three specific areas: education, care for the sick and
            evangelization.
        </p>

        <p>
            The Canossian Sister are in every single one of the world's
            continents, making it 35 countries altogether. In Brazil, theyre in
            five states: São Paulo, Rio de Janeiro, Minas Gerais, Santa Catarina
            and Maranhão. The arrival of the sisters in Brazil was in 1948 in the
            city of Santos and 1978 in Imperatriz.
        </p>
