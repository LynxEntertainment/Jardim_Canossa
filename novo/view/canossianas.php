        <p>
            <b>Congregação das Filhas da Caridade Canossianas – Irmãs Canossianas</b>
        </p>

        <p>
            As irmãs Canossianas foram fundadas por Madalena de Canossa, em 
            Verona-Itália em 08 de maio de 1808. Desenvolve atividades em três 
            áreas específicas: Educação, Assistência aos doentes e Evangelização.
        </p>

        <p>
            As irmãs Canossianas estão presentes em cada um dos continentes do 
            planeta, totalizando 35 países ao todo. No Brasil, estão presentes 
            em cinco estados: São Paulo, Rio de Janeiro, Minas Gerais, Santa 
            Catarina e Maranhão. A chegada das irmãs no Brasil - 1948 na cidade 
            de Santos A chegada das irmãs em Imperatriz - 1978
