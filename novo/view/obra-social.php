        <p>
            <b>OBRA SOCIAL MADALENA DE CANOSSA – ARARAS - SP</b>
        </p>
        <p>
            É uma Entidade Filantrópica que atende gratuitamente crianças e 
            adolescentes na idade de 06 a 15 anos na periferia do Município de 
            Araras. A Entidade presta serviços de Proteção Social Básica - 
            Serviços de Convivência e Fortalecimento de Vínculos. As atividades 
            são de 2ª a 6ª feira no contra turno escolar nos períodos matutino e
            vespertino. As atividades são desenvolvidas de forma lúdica, 
            cultural e esportiva através de oficinas: - Artesanato e trabalhos 
            manuais (Pintura em tecido e Artes recicláveis, Bordado e primeiros 
            pontos de costura, Tear, Tricô e Crochê, Biscuit), Culinária, Oficina 
            Golfinho (natação), Oficina SuperAção: modalidades esportivas 
            (Volei), Capoeira, Ballet, Música, Tra-la-lá (Recreação), Vamos 
            contar (Contadoras de Histórias), Bem-me-quer (Cidadania e meio 
            ambiente e Projeto D.A.M.A. - Despertado e Aperfeiçoando a Mulher do
            Amanhã).
        </p>

        <p>
            NOSSA MISSÃO: investir na formação do Coração acreditando que a 
            Educação e a Formação humana é o centro propulsor do qual depende a 
            conduta de toda vida.
        </p>
