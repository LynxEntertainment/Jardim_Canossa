        <p>
            <b>MAGDALENE OF CANOSSA SOCIAL WORK - ARARAS - SP</b>
        </p>
        <p>
            It is an philantropic endity that attends children and adolescents
            from 6 to 15 years old in the outskirts of the Araras county. The
            entity provides basic social protection services - cohabitation and
            bond strengthening services. The activities are run from monday to
            friday on the reverse school shift, in the morning and afternoon.
            The activities are developed on a playful, cultural and sporting way
            though the workshops: crafts and handwork (cloth painting,
            recyclable arts, stitching, skewing, looming, knitting, crochet and
            biscuit), cooking, Golfinho Workshop (swimming), SuperAção workshop
            (sports like volley, capoeira, ballet), music, Tra-la-lá
            (recreation), let's tell(storytellers), Bem-me-quer (citizenship,
            environment and D.A.M.A project - Despertando e Aperfeiçoando a
            Mulher do Amanhã).

        </p>
        <p>
            OUR MISSION: invest on the heart's formation, believing that
            education and human formation are the center propelling of which
            depends the whole life conduct.
        </p>
