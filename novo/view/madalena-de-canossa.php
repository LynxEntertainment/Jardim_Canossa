    <div data-idioma="ita" id="conteudo-coluna-2">
        <p>
            <b>MADDALENA GABRIELLA DI CANOSSA</b>
        </p>

        <p>
            Maddalena nasce il giorno 01 di marzo del 1774 a Verona, Italia. É 
            La terzogenita figlia del marchese Ottavio e della contessa Tereza 
            Szluha, battezzada il giorno 02 dello stesso mese nella Parrocchia 
            di San Lorenzo. A 5 anni perde il papá. Due anni dopo, la mamma 
            straniera lascia il Palazzo Canossa per sposarsi un´altra volta. 
            Maddalena e i suoi fratelli: Laura, Bonifacio, Rosa e Eleonora 
            vengono cresciuti dal nonno e zii paterni.
        </p>

        <p>
            A 14 anni il vaiolo, quasi la porta alla morte. A 17 anni nasce in 
            lei il desiderio di diventare monaca e chiede alla famiglia di poter
            entrare nel Monastero delle Carmelite. Per ben due volte andrá e 
            tornerá in casa e a partir da quel momento inizierá una direzione 
            spirituale con Don Libera con il quale manterrá una assídua 
            corrispondenza epistolare per alcuni anni finqué scoprirá la sua 
            vocazione. A 25 anni Maddalena passa ad amministrare il Palazzo e a 
            curare la sua famiglia. Allo stesso tempo approfitta per unirsi ai 
            movimenti volontari che facevano visita agli ospedali e che accudiva
            agli ammalati, como pure aiutava nell´opera di evangelizzazione 
            della sua Parrocchia. 
        </p>

        <p>
            Nel bel mezzo della guerra napoelonica, nell´inizio del secolo XIX,
            con 34 anni, Maddalena di Canossa decide di iniziare un´azione
            umanistica nel quale avrebbe accolto bambine abbandonate/orfane di
            Verona.
        </p>

        <p>
            Lasció il Palazzo nel quale viveva e fu ad abitare in una casa della
            periferia della cittá, nel quartiere San Zeno. Si unirono a lei 
            algune ragazze che l´aiutarono a promuovere queste ragazzine orfane.
            Nasce cosi il giorno 08 di maggio del 1808, la Congregazione delle 
            Figlie della Caritá Canossiane, conosciute come Suore Canossiane. 
            Che si dedicarono a educare, all´attenzione con gli ammalati e 
            all´evangelizzazione. Il lavoro svolto da Maddalena di Canossa 
            divenne di riferimento nella cittá di Verona e in altre cittá e 
            attraverso il Vescovo, Maddalena venne sollecitata per opere anche
            nelle cittá di Venezi, Trento, bergamo e Milano.
        </p>

        <p>
            Nel secolo XIX, le donne che desideravano essere Suore avevano solo 
            la possibilitá di diventare suore di clausura. Maddalena inizió un
            nuovo stile di vita religiosa: la vita attiva, cioé di quelle
            religiose che lavoravano nelle aree di educazione, salute e 
            assistenza. Il sogno di Maddalena era di realizzare un lavoro con i 
            bambini, perché fino a quel momento l´azione si riduceva solo alle 
            bambine. Nel 1831 riuscí con due giovani laici falegnami a iniziare 
            l´opera con i bambini, nacque a partire da qui i Figli della Caritá
            Canossiani (Sacerdoti e Fratelli) .
        </p>

        <p>
            Maddalena di Canossa svolse un lavoro molto ampio oltre a promuovere
            l´infanzia e la gioventú si dedicava a preparare le ragazze in
            conoscenze generali per essere moltiplicatrici nelle realtá rurali
            dove vivevano. Si dedicó intensamente a aprire scuole di educazione 
            infantile nelle localitá che passava. Sensibilizzava le persone 
            benestanti per aiutare i piú vulnerabili e poveri.
        </p>

        <p>
            Maddalena venne a morire il 10 di aprile 1835, lasciando come 
            ereditá due Congregazioni Religiose. Venne canonizzata il 02 di 
            Ottobre del 1988.
        </p>
