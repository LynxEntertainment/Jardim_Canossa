    <div data-idioma="por" id='conteudo-coluna-2'>
        <p>
            <b>MADALENA GABRIELA DE CANOSSA</b>
        </p>
        <p>
            Madalena nasceu dia 01 de março de 1774 em Verona, na Itália. Foi 
            a terceira filha do marquês Otávio e da condessa Teresa Szluha, batizada
            no dia 02 de março na paróquia de São Lourenço. Aos 5 anos de idade 
            perdeu o pai. Dois anos depois, sua mãe - que era estrangeira na Itália 
            - deixou o palácio de Canossas para se casar novamente. Madalena e seus 
            irmãos: Laura, Bonifácio, Rosa e Leonor foram criados pelo avó e tios 
            paternos.
        </p>

        <p>
            Aos 14 anos teve febre varíola, quase vindo a óbito. Aos 17 anos 
            nasce o desejo de ser monja, fazendo com que pedisse à família para 
            entrar no Mosteiro das Carmelitas. Por duas vezes ela vai e volta 
            para casa e a partir daí inicia uma direção espiritual com o Padre 
            Líbera – com quem mantém correspondência sistemática por alguns anos
            até descobrir qual a sua vocação. Aos 25 anos de idade Madalena 
            passa a ser a administradora do Palácio e a zelar pela sua família. 
            Ao mesmo tempo aproveitava para se agregar ao movimento voluntário 
            que fazia visitas aos hospitais e cuidava dos doentes, bem como 
            auxiliava na obra de evangelização de sua paróquia. Em meio à guerra
            napoleônica, no inicio do século XIX, com 34 anos de idade, Madalena
            de Canossa decidiu iniciar uma ação humanística na qual iria acolher
            as crianças abandonadas/órfãs nas ruas de Verona.
        </p>

        <p>
            Deixou o palácio no qual vivia e foi morar numa casa na periferia da
            cidade, no bairro São Zeno. Juntaram-se a ela algumas moças que se 
            puseram a cuidar dessas meninas órfãs. Nasceu então - em 08 de maio 
            de 1808 - a Congregação das Filhas da Caridade Canossianas, 
            conhecidas como Irmãs Canossianas. Dedicavam-se a educar, cuidar da
            saúde e a evangelizar. O trabalho desenvolvido por Madalena de 
            Canossa tornou-se referencial na cidade de Verona e em outras 
            cidades – através do Sr. Bispo - solicitaram à Madalena também uma 
            obra nas cidades de Veneza, Trento, Bérgamo e Milão.
        </p>

        <p>
            No século XIX, as mulheres que queriam ser religiosas (freiras) 
            tinham apenas a opção dos mosteiros de clausura (vida contemplativa 
            fechada). Madalena iniciou um novo estilo de vida religiosa: a de 
            vida ativa, aquelas religiosas que trabalhavam na área da educação, 
            saúde e assistência. O sonho de Madalena era atingir também o 
            trabalho com os meninos, pois até então o seu trabalho se reduzia 
            às meninas. Em 1931 conseguiu dois irmãos marceneiros, que não eram 
            padres para iniciar um trabalho com os meninos. Nasceu a partir daí 
            a congregação dos Filhos da Caridade Canossianos (padres e irmãos) 
            que passaram a se dedicar às crianças, adolescentes, jovens e 
            adultos.
        </p>

        <p>
            Madalena de Canossa desenvolveu um trabalho muito amplo além de 
            promover a infância e a juventude dedicava-se à preparação de moças 
            quanto à alfabetização para serem multiplicadoras em suas 
            realidades. Dedicou-se intensamente abrindo escolas de educação 
            infantil em todos os lugares em que passava. Sensibilizava a classe
            mais abastada para auxiliar os mais vulneráveis e pobres. Madalena 
            morreu em 10 de abril de 1935, deixando o legado de duas 
            congregações religiosas. Foi canonizada (santa) em 02 de outubro de 
            1988.
        </p>
