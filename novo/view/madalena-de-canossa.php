    <div data-idioma="eng" id='conteudo-coluna-2'>
        <p>
            <b>MADDALENA GABRIELLA OF CANOSSA</b>
        </p>
        <p>
            Magdalene was born on march 1<sup>st</sup> 1774 in Verona, Italy.
            was the third daughter of marquess Otavio and countess Tereza Szluha,
            baptized in march 2<sup>nd</sup> in the parish of St. Lawrence. Lost
            her father at 5 years old, and her mother - who was a foreigner in
            italy - left the palace of Canossas 2 years later to get married
            again. Magdalene and her brothers, Laura, Bonifacio, Rosa and Leonor
            were raised by their grandmother and paternal uncles.
        </p>
        <p>
            At 14 almost died of fever and variola. At 17 is born the desire to
            be a monk, making her ask her family to join the Monastery of
            Carmelitas. Twice she goes and comes back home, then starts a
            spiritual direction with Father Líbera - with whom she keeps
            systematic correspondence for a few years until she finds her
            vocation. At 25, Magdalene becomes administrator of the palace, now
            responsible for looking after her family. At the same time taked to
            join the voluntary movement, which visited hospitals and cared for
            the sick, as well as helped with the evangelization work of her
            parish. In mid of the napoleonic war, in the early XIX century, with
            34 years old, Magdalene of Canossa decide to start a humanistic
            action which would welcome abandoned and orphan children on the
            streets of Verona.
        </p>

        <p>
            She left the palave where she lived and went to live in a house on
            the outskirts of the city, in Saint Zeno district. Along with her a
            few young women started to care for those orphan girls. Is born
            then - in may 8<sup>th</sup> 1808 - the Canossian Daughters of
            Charity congregation, also known as Canossian Sisters. They
            dedicated to educate, care for health and evangelize. The work
            developed by Magadalene of Canossa became a reference in the city of
            Verona and other cities and - by Mr. Bishop - she was requested to
            bring her work to Veneza, Trento, Bergamo and Milan.
        </p>

        <p>
            In the XIX century, women who wanted to become religious (nuns) had
            only the option of the closure monasteries (closed contemplative
            life). Magdalene started a new religious life style: the active life,
            those religious who worked on education, health and assistance. The
            dream of Magdalene was to also reach teh work with teh boys, because
            then it was limited to girls. In 1931 she got two woodworker brohers,
            who were not priests to start working with boys. Is born then, the
            Canossian Sons of Charity congregation (fathers and brothers) who
            started dedicating to children, adolescents, young and adults.
        </p>

        <p>
            Magdalene of Canossa developed a very wide work in addition to
            promoting childhood and youth, she dedicated to teh preparation of
            young womenc concerning literacy to be multiplyers into their
            realities. She intensely dedicated opening schools of child
            education everywhere she passed by. Sensitized the most wealthy
            class to help the vulnerable and the poor. Magdalene died in april
            10<sup>th</sup> 1935, leaving a legacy of two religious congregations.
            She was canonized (saint) in october 2<sup>nd</sup> 1988.
        </p>
