        <p>
            The Monsignor Quércia School was founded in march 19<sup>th</sup>
            1960 by the Canossian Sisters, situated on Henrique Dias St. 400,
            Belvedere, Araras, SP. It is an educational institution
            confessionally catholic and apolitical. It's main goal is the
            "Heart's Formation". The heart is the main propellant of the person.
            The educative action developed aims to form the person from the
            inside for human, christian, ethic and moral values, leading it to
            exercise it's citizenship and become the subject of it's own history,
            such as desired by Saint Magdalene of Canossa, founder of the
            Canossian Sisters.
        </p>
        <p>
            The school offers:
        </p>
        
        <ul>
            <li>Child Education - children from 3 to 6 years old</li>
            <li>Elementary School I - from years 1 to 5.</li>
            <li>Elementary School II - from years 6 to 9.</li>
        </ul>
        
