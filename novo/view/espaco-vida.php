    <div data-idioma="por" id='conteudo-coluna-2'>
        <p>
            O Espaço Vida é um dos projetos desenvolvidos pela Associação 
            Madalena de Canossa. Consiste em um projeto socioassistencial 
            voltado para jovens e adultos (na primeira fase, contemplava 
            crianças e adolescentes de 07 a 15 anos). Iniciado em 2008, teve as 
            atividades suspensas no ano de 2012 devido a problemas financeiros.
        </p>

        <p>
            No ano de 2015 a AMC passou a integrar uma lista de possíveis instituições 
            beneficentes que receberiam doações para manutenção de projetos 
            filantrópicos em Imperatriz.
        </p>

        <p>Após tomarem conhecimento dos trabalhos desenvolvidos pelas Irmãs Canossianas, 
            os doadores escolheram a AMC para receber a ajuda e, ainda em 2016, retomar 
            as atividades do Espaço Vida.
        </p>

        <p>Nesse novo formato, o espaço irá abranger não somente o público infantil. A 
            pretensão é que crianças até idosos possam participar das atividades 
            promovidas pelo Espaço Vida.
        </p>

        <p>Para o reinicio das atividades, previstas para o mês de agosto, teremos 
            cursos de Informática e Corte e Costura.
        </p>
    </div>
    <div data-idioma="eng" id='conteudo-coluna-2'>
        <p>
            The Life Space is one of the projects developed by the Madalena de
            Canossa Association. It consists of a social assistance project
            aimed at the young and adults (at the first stage, it contemplated
            children and teenagers from 7 to 15 years old). Started in 2008, had
            it's activities suspended in 2012 because of financial problems.
        </p>
        <p>
            In 2015, AMC started integrating a list of possible charity that
            could receive donations for the maintainance of philantropic
            projects in Imperatriz.
        </p>

        <p>
            After acknowledging the work developed by the Canossian Sisters,
            donators have chosen AMC to help, and in 2016, Life Space resumed
            it's activities.
        </p>

        <p>
            Under this new format, the space will include not only the children,
            but all ages from children to the elder. The goal is that anyone can
            participate of the activities promoted by Life Space.
        </p>

        <p>
            To resume the activities, we have fixed to august, computing and
            cut-and-sew courses.
        </p>
    </div>
