        <p>
            Lo Spazio di Vita é uno dei progetti svolti dall´Associazione 
            Maddalena di Canossa. Consiste in un Progetto socio assistenziale 
            rivolto ai giovani e adulti (inizialmente era per i ragazzi daí 07 
            ai 15 anni). É stato iniziato nel 1999, e per problemi finanziari 
            nel 2012 le attivitá sono state sospese.
        </p>

        <p>
            Nell´anno 2015 la AMC ha integrato una lista di possibili 
            istituzioni beneficenti que potrebbero ricevere donazioni per la 
            manutenzione dei Progetti filantropici in Imperatriz.
        </p>

        <p>
            I donatori dopo aver conosciuto le attivitá sociali delle Suore 
            Canossiane hanno scelto di contribuire per la AMC e cosí nel 2016 
            ha potuto riprendere le attivitá dello Spazio di Vita.
        </p>

        <p>
            In questa nuova apertura si potrá attingere non solo i ragazzi ma 
            anche gli anziani.
        </p>

        <p>
            La ripresa delle attivitá é prevista per il mese di agosto con corsi
            di Informatica e di Cucito.
        </p> 
