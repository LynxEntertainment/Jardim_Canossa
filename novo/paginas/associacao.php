    <div data-idioma="por" id='conteudo-coluna-2'>
        <p>
            Fundada em 16 de junho de 1959, a Associação Madalena de Canossa 
            (AMC) tem sede na cidade de Araras – SP. A Entidade tem como 
            finalidade desenvolver atividades nas áreas de Educação e 
            Assistência Social.
        </p>

        <p>
            As atividades das Irmãs Canossianas naquela cidade haviam iniciado 
            em 1952 onde as mesmas auxiliavam na administração interna do 
            Hospital São Lúis – Casa de Misericórdia, mas ainda não possuíam 
            estrutura física específica para desenvolver as ações sociais.
        </p>

        <p>
            Monsenhor Quércia (padre Francisco Quércia Sobrinho) organizou uma 
            campanha junto ao poder público e a sociedade civil para a 
            construção de uma casa, onde as Irmãs Canossianas poderiam desenvolver suas atividades educativas e pastorais e assim realizou-se oficialmente a criação da AMC em 1959.</p>

        <p>
            O colégio Monsenhor Quércia nasceu em 1960, a Obra de Assistência 
            Social simultaneamente. No que diz respeito às ações voltadas para 
            a educação, a AMC foca nas crianças, adolescentes, mulheres em 
            situação de vulnerabilidade, não medindo esforços para promover 
            qualidade de vida.
        </p>

        <p>
            <b>FILIAL DE IMPERATRIZ - MA - CNPJ: 44.219.723/0002-32 CHAMA-SE</b>
            – Centro De Educação Infantil Madalena De Canossa (Razao Social) e 
            Fantasia – Jardim Canossa.<br>Nos anos noventa outra filial é aberta e 
            dessa vez a de Imperatriz – MA. A presidente da AMC é a Ir. Regina 
            Aparecida Cândido, a representante legal da filial de Imperatriz – 
            Ir. Sandra Maria Nolêto Pargas.
        </p>

        <p>
            <b>Missão</b> - Trabalhar com empenho a formação integral da pessoa humana, 
            privilegiando a formação do coração. O estilo da educação 
            Canossianas expressa de maneira harmônica: amabilidade, paciência, 
            firmeza, familiaridade e respeito, gratuidade e alegria.
        </p>

        <p>
            <b>Visão</b> - Ser referência que potencializa a formação de pessoas 
            justas, solidárias, sensíveis, respeitosas e alegres, desenvolvendo 
            as próprias capacidades, tornando-se protagonistas na valorização e 
            defesa da vida.
        </p>
