            to develop social work.
        </p>
        <p>
            Monsignor Quércia (father Francisco Quércia Sobrinho) arranged a
            campaign next to the públic power and the civil society to build a
            house, where the Canossian Sisters could develop their educational
            and pastoral activities, and then the creation of AMC was ofdicially
            realized in 1959.
        </p>

        <p>
            The Monsenhor Quércia school was built in 1960, simultaneously with
            the Social Assistance Work. Concerning the educational activities,
            AMC fucuses in children, teenagers, and women in vulnerable
            situation, not measuring efforts to promote life quality.
